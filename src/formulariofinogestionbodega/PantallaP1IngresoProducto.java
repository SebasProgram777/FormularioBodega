    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        String nombre = campoNombre.getText().trim();
        String codigo = campoCodigo.getText().trim();
        String cantidad = campoCantidad.getText().trim();
        String precio = campoPrecio.getText().trim();

        if (nombre.isEmpty() || codigo.isEmpty() || cantidad.isEmpty() || precio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter("productos.txt", true))) {
            writer.write(nombre + "," + codigo + "," + cantidad + "," + precio);
            writer.newLine();
            JOptionPane.showMessageDialog(this, "Producto guardado correctamente");
            campoNombre.setText("");
            campoCodigo.setText("");
            campoCantidad.setText("");
            campoPrecio.setText("");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el producto", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

